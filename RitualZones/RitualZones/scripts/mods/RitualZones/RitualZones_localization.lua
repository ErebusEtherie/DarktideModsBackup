	"es",
	"it",
	"ru",
	"pt-br",
	"zh-cn",
	"zh-tw",
	"ja",
	"ko",
	"pl",
}

for _, lang in ipairs(fallback_languages) do
	for _, entry in pairs(localization) do
		if entry.en and entry[lang] == nil then
			entry[lang] = entry.en
		end
	end
end

return localization













